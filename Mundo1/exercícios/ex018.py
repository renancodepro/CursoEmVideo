import math
a = float(input('Digite um 창ngulo: '))
coseno = math.cos(math.radians(a))
seno = math.sin(math.radians(a))
tang = math.tan(math.radians(a))
print(f'O 창ngulo de {a} tem o seno de {seno:.2f} ')
print(f'O 창ngulo de {a} tem o coseno de {coseno:.2f} ')
print(f'O 창ngulo de {a} tem a tangente de {tang:.2f} ')
