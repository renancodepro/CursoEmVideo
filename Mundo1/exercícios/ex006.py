n = int(input('Digite um número : '))
d = n * 2
t = n * 3
r = n ** (1/2)  # n elevado a meio é uma das formas de descobrir raiz quadrada
print(f'O dobro de {n} é : {d}')
print(f'O triplo de {n} é : {t}')
print(f'A raiz quadrada de {n} é igual: {r:.2f}')
